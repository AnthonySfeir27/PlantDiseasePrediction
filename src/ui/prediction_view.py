"""UI rendering for uploaded images and prediction results."""

from PIL import Image
import streamlit as st

from src.models.prediction_result import PredictionResult


def render_uploaded_image(uploaded_file) -> Image.Image:
    """Display the uploaded leaf image and return it as a PIL image."""
    image = Image.open(uploaded_file).convert("RGB")

    left_column, right_column = st.columns([1, 1])
    with left_column:
        st.subheader("Uploaded Image")
        st.image(image, caption=uploaded_file.name, width="stretch")

    with right_column:
        st.subheader("Image Details")
        st.write(f"**File name:** {uploaded_file.name}")
        st.write(f"**Image size:** {image.width} x {image.height}")
        st.write(f"**Image mode:** {image.mode}")
        st.write("**Expected input:** one clear tomato leaf image")

    return image


def render_prediction_result(prediction: PredictionResult) -> None:
    """Display disease prediction, confidence, and recommendation details."""
    st.divider()
    st.subheader("Prediction Result")

    if prediction.is_real_model:
        st.success("Prediction generated by the trained TensorFlow CNN model.")
    else:
        st.warning("Demo prediction shown. Train the model to enable real AI predictions.")

    result_column, details_column = st.columns([1, 2])

    with result_column:
        st.metric("Predicted Class", prediction.disease_name)
        st.metric("Confidence", f"{prediction.confidence:.1f}%")
        st.metric("Severity", prediction.severity)
        st.progress(min(int(prediction.confidence), 100))

    with details_column:
        st.write("**Description**")
        st.write(prediction.description)
        st.write("**Recommended Action**")
        st.write(prediction.recommendation)


def render_training_outputs() -> None:
    """Show saved training charts if they exist."""
    from src.config import MODEL_DIR

    accuracy_plot = MODEL_DIR / "accuracy_plot.png"
    loss_plot = MODEL_DIR / "loss_plot.png"

    if not accuracy_plot.exists() and not loss_plot.exists():
        return

    st.divider()
    st.subheader("Training Results")
    columns = st.columns(2)

    if accuracy_plot.exists():
        columns[0].image(str(accuracy_plot), caption="Accuracy Curve", width="stretch")
    if loss_plot.exists():
        columns[1].image(str(loss_plot), caption="Loss Curve", width="stretch")
